        // TODO add your handling code here:
        Integer id=Integer.parseInt(txtID.getText());
       int  DiaConsulta,MesConsulta,AnioConsulta,DiaNacimiento,MesNacimiento,AnioNacimiento;
        DiaConsulta=Integer.parseInt(txtDiaConsulta.getText());
        MesConsulta=Integer.parseInt(txtMesConsulta.getText());
        
        AnioConsulta=Integer.parseInt(txtAnioConsulta.getText());
        Calendar FechaConsulta= new GregorianCalendar(AnioConsulta,MesConsulta,DiaConsulta);
        
        Medico medico= new Medico(txtTitulo.getText(),txtEspecialidad.getText());
        float Peso,Estatura;
        Peso=Float.parseFloat(txtPeso.getText());
        Estatura=Float.parseFloat(txtEstatura.getText());
        
        Domicilio domicilio= new Domicilio(txtCalleNum.getText(), txtColonia.getText(), txtCodigoPostal.getText(), txtCiudad.getText());
        
        DiaNacimiento=Integer.parseInt(txtDiaNacimiento.getText());
        MesNacimiento=Integer.parseInt(txtMesNacimiento.getText());
      
        AnioNacimiento=Integer.parseInt(txtAnioNacimiento.getText());
        Calendar FechaNacimiento= new GregorianCalendar(AnioNacimiento,MesNacimiento,DiaNacimiento);
        
        String estado1=String.valueOf(txtEstado.getText().toUpperCase());
       Estado estado= Enum.valueOf(Estado.class,estado1);
         String tipodesangre=String.valueOf(T_sangre.getSelectedItem());
        Paciente paciente = new Paciente(txtNSS.getText(), TipodeSangre.O_POSITIVO, Peso, Estatura, txtNombre.getText() ,txtApellido1.getText(),txtApellido2.getText(),txtSexo.getText(), estado, txtHomo.getText(), domicilio,FechaNacimiento);
        
        Consulta consulta = new Consulta(id, FechaConsulta, medico, paciente, txtSintomas.getText(),txtDiagnostico.getText(),txtReceta.getText());
        listaconsultas.add(consulta);
        
        try {
      FileOutputStream fileOut = new FileOutputStream("Consulta.ser");
      ObjectOutputStream out = new ObjectOutputStream(fileOut);
      out.writeObject(listaconsultas);
      out.close();
      fileOut.close();
    } catch (IOException i) {
      i.printStackTrace();
    }
         Modificar.setEnabled(true);
         Eliminar.setEnabled(true);
         Listar.setEnabled(true);
        
        
