               diaConsulta=Integer.parseInt(txtDiaConsulta.getText());
               Calendar fechaConsulta = new GregorianCalendar(AnioConsulta,MesConsulta,diaConsulta);
               consulta.setFecha(fechaConsulta);
               
               consulta.paciente.curp(txtApellido1.getText(), txtApellido2.getText(), txtNombre.getText(), txtSexo.getText(),estado.getEstados());
               
